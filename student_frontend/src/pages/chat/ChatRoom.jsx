import React, { useState, useEffect, useRef } from 'react';
import { useSelector, useDispatch } from 'react-redux';
import { Dialog, DialogTitle, DialogContent, DialogActions, Button, TextField, Box, List, ListItemButton, ListItemText, Typography, Autocomplete, IconButton } from '@mui/material';
import { Close as CloseIcon } from '@mui/icons-material';
import { selectChatRoom, sendMessage, fetchChatRooms, createChatRoom, leaveChatRoom } from '@/redux/chat/chatSlice';
import { API_URL } from "@/constant";
import { fetchWithAuth } from "@features/auth/utils/fetchWithAuth";
import { showSnackbar } from "@/redux/snackbarSlice";
import useDebounce from "@hook/useDebounce";
import { Stomp } from '@stomp/stompjs';
import SockJS from 'sockjs-client';

const ChatRoom = ({ onClose }) => {
    const dispatch = useDispatch();
    const { chatRooms, selectedRoom, messages } = useSelector(state => state.chat);
    const { user } = useSelector(state => state.auth);
    const [newMessage, setNewMessage] = useState('');
    const [openNewChatModal, setOpenNewChatModal] = useState(false);
    const [selectedUser, setSelectedUser] = useState(null);
    const [users, setUsers] = useState([]);
    const [searchQuery, setSearchQuery] = useState("");
    const debouncedQuery = useDebounce(searchQuery, 300);
    const messagesEndRef = useRef(null);
    const [isTyping, setIsTyping] = useState({});
    const [stompClient, setStompClient] = useState(null);

    useEffect(() => {
        dispatch(fetchChatRooms());
    }, [dispatch]);

    useEffect(() => {
        if (selectedRoom) {
            dispatch(selectChatRoom(selectedRoom));
        }
    }, [dispatch, selectedRoom]);

    useEffect(() => {
        messagesEndRef.current?.scrollIntoView({ behavior: "smooth" });
    }, [messages]);

    useEffect(() => {
        if (debouncedQuery.length >= 2) {
            fetchUsers(debouncedQuery);
        } else {
            setUsers([]);
        }
    }, [debouncedQuery]);

    // WebSocket Ïó∞Í≤∞ ÏÑ§Ï†ï
    useEffect(() => {
        const socket = new SockJS(`${API_URL}ws`);
        const client = Stomp.over(socket);

        client.connect({}, () => {
            console.log('WebSocket Ïó∞Í≤∞ ÏÑ±Í≥µ');
            setStompClient(client);

            if (selectedRoom) {
                subscribeTyping(client, selectedRoom);
            }
        }, (error) => {
            console.error('WebSocket Ïó∞Í≤∞ Ïò§Î•ò:', error);
        });

        return () => {
            if (client) {
                client.disconnect(() => {
                    console.log('WebSocket Ïó∞Í≤∞ Ìï¥Ï†ú');
                });
            }
        };
    }, [selectedRoom]);

    useEffect(() => {
        if (stompClient) {
            stompClient.unsubscribe(`/topic/chat.typing`);
            subscribeTyping(stompClient, selectedRoom);
        }
    }, [selectedRoom, stompClient]);

    const subscribeTyping = (client, roomId) => {
        if (roomId) {
            client.subscribe(`/topic/chat.typing`, (message) => {
                const receivedMessage = JSON.parse(message.body);
                if (receivedMessage.senderId !== user.id) {
                    setIsTyping(prev => ({
                        ...prev,
                        [roomId]: receivedMessage.senderId
                    }));
                }
                setTimeout(() => {
                    setIsTyping(prev => {
                        const newState = { ...prev };
                        delete newState[roomId];
                        return newState;
                    });
                }, 3000);
            });
        }
    };

    const sendTyping = (roomId) => {
        if (stompClient) {
            const message = {
                roomId: roomId,
                senderId: user.id,
                typing: true
            };
            stompClient.send("/app/chat.typing", {}, JSON.stringify(message));
        }
    };

    const fetchUsers = async (query) => {
        if (!query) return;

        try {
            const response = await fetchWithAuth(`${API_URL}members/search?query=${query}`);
            if (response.ok) {
                const data = await response.json();
                setUsers(data.data || []);
            } else {
                setUsers([]);
            }
        } catch (error) {
            console.error("üö® ÏÇ¨Ïö©Ïûê Í≤ÄÏÉâ Ïã§Ìå®:", error.message);
            setUsers([]);
        }
    };

    const handleSendMessage = () => {
        if (newMessage.trim() && selectedRoom) {
            dispatch(sendMessage({
                roomId: selectedRoom,
                content: newMessage
            }));
            setNewMessage('');
        } else {
            dispatch(showSnackbar("‚ùå Ï±ÑÌåÖÎ∞©ÏùÑ ÏÑ†ÌÉùÌï¥Ï£ºÏÑ∏Ïöî."));
        }
    };

    const handleCreateNewChat = async () => {
        if (!selectedUser) {
            dispatch(showSnackbar("‚ùå ÎåÄÌôî ÏÉÅÎåÄÎ•º ÏÑ†ÌÉùÌï¥Ï£ºÏÑ∏Ïöî."));
            return;
        }
        try {
            await dispatch(createChatRoom({
                name: `${user.name}, ${selectedUser.name}`,
                participantIds: [selectedUser.id]
            })).unwrap();

            setOpenNewChatModal(false);
            setSelectedUser(null);
            dispatch(showSnackbar("‚úÖ ÏÉàÎ°úÏö¥ Ï±ÑÌåÖÎ∞©Ïù¥ ÏÉùÏÑ±ÎêòÏóàÏäµÎãàÎã§."));
        } catch (error) {
            console.error("üö® Ï±ÑÌåÖÎ∞© ÏÉùÏÑ± Ïã§Ìå®:", error.message);
            dispatch(showSnackbar("‚ùå Ï±ÑÌåÖÎ∞© ÏÉùÏÑ±Ïóê Ïã§Ìå®ÌñàÏäµÎãàÎã§."));
        }
    };

    const handleLeaveChatRoom = async (roomId) => {
        try {
            await dispatch(leaveChatRoom(roomId)).unwrap();
            dispatch(showSnackbar("‚úÖ Ï±ÑÌåÖÎ∞©ÏóêÏÑú ÎÇòÍ∞îÏäµÎãàÎã§."));
        } catch (error) {
            console.error('Ï±ÑÌåÖÎ∞© ÎÇòÍ∞ÄÍ∏∞ Ïò§Î•ò:', error);
            dispatch(showSnackbar("‚ùå Ï±ÑÌåÖÎ∞© ÎÇòÍ∞ÄÍ∏∞Ïóê Ïã§Ìå®ÌñàÏäµÎãàÎã§."));
        }
    };

    const handleTyping = (roomId) => {
        sendTyping(roomId);
    };

    return (
        <Dialog onClose={onClose} open={true} maxWidth="md" fullWidth>
            <DialogTitle>Ï±ÑÌåÖ</DialogTitle>
            <DialogContent dividers sx={{ display: 'flex', height: 400, p: 0 }}>
                <Box sx={{ width: '30%', borderRight: '1px solid #ccc', overflowY: 'auto' }}>
                    <Button
                        fullWidth
                        variant="contained"
                        color="primary"
                        onClick={() => setOpenNewChatModal(true)}
                        sx={{ mb: 1 }}
                    >
                        ÏÉà Ï±ÑÌåÖ ÏãúÏûë
                    </Button>
                    {chatRooms.map(room => (
                        <ListItemButton
                            key={room.id}
                            selected={selectedRoom === room.id}
                            onClick={() => dispatch(selectChatRoom(room.id))}
                        >
                            <ListItemText primary={room.name} />
                            {selectedRoom === room.id && (
                                <IconButton onClick={(e) => {
                                    e.stopPropagation();
                                    handleLeaveChatRoom(room.id);
                                }}>
                                    <CloseIcon />
                                </IconButton>
                            )}
                        </ListItemButton>
                    ))}
                </Box>
                <Box sx={{ width: '70%', p: 2, display: 'flex', flexDirection: 'column' }}>
                    {selectedRoom ? (
                        <>
                            <Box sx={{ flexGrow: 1, overflowY: 'auto', mb: 1 }}>
                                {messages.map(message => (
                                    <Box key={message.id} sx={{mb: 1}}>
                                        <Typography variant="subtitle2">{message.senderName}</Typography>
                                        <Typography>{message.content}</Typography>
                                        <Typography variant="caption">{new Date(message.timestamp).toLocaleTimeString()}</Typography>
                                    </Box>
                                ))}
                                <div ref={messagesEndRef} />
                            </Box>
                            {isTyping[selectedRoom] && (
                                <Typography variant="subtitle2" color="textSecondary">
                                    {isTyping[selectedRoom]}ÎãòÏù¥ ÏûÖÎ†• Ï§ëÏûÖÎãàÎã§...
                                </Typography>
                            )}
                            <Box sx={{ display: 'flex', alignItems: 'center' }}>
                                <TextField
                                    fullWidth
                                    placeholder="Î©îÏãúÏßÄÎ•º ÏûÖÎ†•ÌïòÏÑ∏Ïöî."
                                    variant="outlined"
                                    size="small"
                                    value={newMessage}
                                    onChange={(e) => {
                                        setNewMessage(e.target.value);
                                        handleTyping(selectedRoom);
                                    }}
                                    onKeyPress={(e) => {
                                        if (e.key === 'Enter') {
                                            handleSendMessage();
                                        }
                                    }}
                                />
                                <Button variant="contained" color="primary" onClick={handleSendMessage} sx={{ ml: 1 }}>
                                    Î≥¥ÎÇ¥Í∏∞
                                </Button>
                            </Box>
                        </>
                    ) : (
                        <Typography variant="subtitle1">Ï±ÑÌåÖÎ∞©ÏùÑ ÏÑ†ÌÉùÌï¥Ï£ºÏÑ∏Ïöî.</Typography>
                    )}
                </Box>
            </DialogContent>
            <DialogActions>
                <Button onClick={onClose}>Îã´Í∏∞</Button>
            </DialogActions>
            {/* New Chat Modal */}
            <Dialog open={openNewChatModal} onClose={() => setOpenNewChatModal(false)} maxWidth="sm" fullWidth>
                <DialogTitle>ÏÉà Ï±ÑÌåÖ ÏãúÏûë</DialogTitle>
                <DialogContent>
                    <TextField
                        label="ÏÇ¨Ïö©Ïûê Í≤ÄÏÉâ"
                        variant="outlined"
                        fullWidth
                        margin="normal"
                        value={searchQuery}
                        onChange={(e) => setSearchQuery(e.target.value)}
                    />
                    {users.length > 0 && (
                        <List>
                            {users.map(user => (
                                <ListItemButton key={user.id} onClick={() => setSelectedUser(user)}>
                                    <ListItemText primary={user.name} />
                                </ListItemButton>
                            ))}
                        </List>
                    )}
                </DialogContent>
                <DialogActions>
                    <Button onClick={() => setOpenNewChatModal(false)}>Ï∑®ÏÜå</Button>
                    <Button variant="contained" color="primary" onClick={handleCreateNewChat}>
                        Ï±ÑÌåÖ ÏãúÏûë
                    </Button>
                </DialogActions>
            </Dialog>
        </Dialog>
    );
};

export default ChatRoom;
